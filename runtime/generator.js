// ── Page Manifest ─────────────────────────────────────────────────────────────

function generatePageManifest(appSpec) {
  const imports = appSpec.pages.map(p =>
    `import ${p.name}Page from './${p.name.toLowerCase()}.jsx';`
  ).join('\n');

  const routes = appSpec.pages.map(p =>
    `  { path: '${p.route}', component: ${p.name}Page, protected: ${p.protected}, roles: ${JSON.stringify(p.roles || [])} },`
  ).join('\n');

  return `// Auto-generated by AppForge — Page manifest
${imports}

export const routes = [
${routes}
];

export default routes;
`;
}

// ── Type helpers ─────────────────────────────────────────────────────────────

function fieldTypeToPrisma(type) {
  const map = {
    string: 'String', number: 'Int', boolean: 'Boolean',
    date: 'DateTime', email: 'String', enum: 'String',
    uuid: 'String', text: 'String', json: 'Json',
    decimal: 'Decimal', password: 'String',
  };
  return map[type] || 'String';
}
