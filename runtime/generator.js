/**
 * Runtime Generator
 * Converts AppSpec IR → actual executable code artifacts.
 * 
 * Generates:
 *   generated/pages/          → Next.js page components
 *   generated/api/            → Express route files
 *   generated/prisma/         → Prisma schema
 *   generated/auth/           → Auth middleware
 * 
 * Then the Simulator validates all generated artifacts.
 */

import { toSnakePlural } from '../types/appSpec.js';
import fs from 'fs';
import path from 'path';

// ── Main entry ────────────────────────────────────────────────────────────────

export function generateArtifacts(appSpec) {
  const artifacts = {};

  artifacts['prisma/schema.prisma'] = generatePrismaSchema(appSpec);
  artifacts['api/routes.js'] = generateAPIRoutes(appSpec);
  artifacts['auth/middleware.js'] = generateAuthMiddleware(appSpec);
  artifacts['pages/index.js'] = generatePageManifest(appSpec);

  for (const page of appSpec.pages) {
    artifacts[`pages/${page.name.toLowerCase()}.jsx`] = generatePageComponent(page, appSpec);
  }

  for (const entity of appSpec.entities) {
  artifacts[`api/${entity.name.toLowerCase()}Router.js`] =
      generateEntityRouter(entity, appSpec);
}


// ── Persist generated artifacts ─────────────────────

const outputDir='./generated';

if(!fs.existsSync(outputDir)){

    fs.mkdirSync(
        outputDir,
        {recursive:true}
    );
}

for(
    const [file,content]
    of Object.entries(artifacts)
){

    const filePath=
        path.join(
            outputDir,
            file
        );

    const directory=
        path.dirname(
            filePath
        );

    fs.mkdirSync(
        directory,
        {recursive:true}
    );

    fs.writeFileSync(
        filePath,
        content
    );
}

  return artifacts;
}

// ── Prisma Schema Generator ───────────────────────────────────────────────────

function generatePrismaSchema(appSpec) {
  const models = appSpec.entities.map(entity => {
    const fields = [
      `  id        String   @id @default(uuid())`,
      ...entity.fields.map(f => {
        const prismaType = fieldTypeToPrisma(f.type);
        const optional = !f.required ? '?' : '';
        const unique = f.unique ? ' @unique' : '';
        const defaultVal = f.type === 'boolean' ? ' @default(false)' : '';
        return `  ${f.name.padEnd(12)}${prismaType}${optional}${unique}${defaultVal}`;
      }),
      ...entity.relations.map(r => {
        if (r.type === 'belongsTo') {
          const fk = r.foreignKey || `${r.entity.toLowerCase()}Id`;
          return `  ${r.entity.toLowerCase().padEnd(12)}${r.entity}  @relation(fields: [${fk}], references: [id])\n  ${fk.padEnd(12)}String`;
        }
        if (r.type === 'hasMany') {
          return `  ${(r.entity.toLowerCase() + 's').padEnd(12)}${r.entity}[]`;
        }
        return `  // ${r.type} ${r.entity}`;
      }),
    ];

    if (entity.timestamps) {
      fields.push(`  createdAt DateTime @default(now())`);
      fields.push(`  updatedAt DateTime @updatedAt`);
    }
    if (entity.softDelete) {
      fields.push(`  deletedAt DateTime?`);
    }

    return `model ${entity.name} {\n${fields.join('\n')}\n}`;
  });

  // Always add User model if auth required
  const hasUser = appSpec.entities.some(e => e.name.toLowerCase() === 'user');
  if (!hasUser) {
    const roleEnum = appSpec.roles.map(r => r.name).join(', ');
    models.unshift(`enum Role {\n  ${appSpec.roles.map(r => r.name.toUpperCase()).join('\n  ')}\n}\n\nmodel User {\n  id           String   @id @default(uuid())\n  email        String   @unique\n  passwordHash String\n  role         Role     @default(${appSpec.roles.find(r => r.isDefault)?.name?.toUpperCase() || 'USER'})\n  createdAt    DateTime @default(now())\n  updatedAt    DateTime @updatedAt\n}`);
  }

  return `// Auto-generated by AppForge
// DO NOT EDIT MANUALLY

generator client {
  provider = "prisma-client-js"
}

datasource db {
  provider = "${appSpec.auth?.strategy === 'session' ? 'sqlite' : 'postgresql'}"
  url      = env("DATABASE_URL")
}

${models.join('\n\n')}
`;
}

// ── API Routes Generator ──────────────────────────────────────────────────────

