// ── Entity Router Generator ───────────────────────────────────────────────────

function generateEntityRouter(entity, appSpec) {
  const entityName = entity.name;
  const varName = entityName.toLowerCase();
  const tableName = entity.tableName;

  // Find endpoints for this entity
  const entityEndpoints = appSpec.endpoints.filter(ep => ep.entity === entityName);
  const hasRoleGuard = (ep) => ep.roles?.length > 0 ? `authorize(${JSON.stringify(ep.roles)})` : null;

  const routes = entityEndpoints.length > 0
    ? entityEndpoints.map(ep => {
        const method = ep.method.toLowerCase();
        const path =
  ep.path.replace(
      new RegExp(`.*\\/${tableName}\\/?`),
      '/'
  ) || '/';
        const guards = [ep.auth ? 'authenticate' : null, hasRoleGuard(ep)].filter(Boolean).join(', ');
        const guardsStr = guards ? `${guards}, ` : '';

        return `
// ${ep.description}
router.${method}('${path}', ${guardsStr}async (req, res) => {
  try {
    // TODO: implement with Prisma
    // const result = await prisma.${varName}.${ep.operation === 'list' ? 'findMany' : ep.operation === 'get' ? 'findUnique' : ep.operation}({ ... });
    res.json({ message: '${ep.description}', success: true });
  } catch (err) {
    res.status(500).json({ error: err.message });
  }
});`;
      }).join('\n')
    : `
// CRUD for ${entityName}
router.get('/', authenticate, async (req,res)=>{

  try{

    const items=
      await prisma.${varName}.findMany();

    res.json({
      items,
      total:items.length
    });

  }catch(err){

    res.status(500).json({
      error:err.message
    });

  }

});

router.post('/', authenticate, async(req,res)=>{

  try{

    const created=
      await prisma.${varName}.create({
          data:req.body
      });

    res.status(201).json(created);

  }catch(err){

      res.status(500).json({
          error:err.message
      });

  }

});

router.get('/:id', authenticate, async(req,res)=>{

  try{

      const item=
        await prisma.${varName}.findUnique({
            where:{
                id:req.params.id
            }
        });

      res.json(item);

  }catch(err){

      res.status(500).json({
          error:err.message
      });

  }

});

router.put('/:id', authenticate, async(req,res)=>{

 try{

      const updated=
        await prisma.${varName}.update({

            where:{
                id:req.params.id
            },

            data:req.body

        });

      res.json(updated);

 }catch(err){

      res.status(500).json({
          error:err.message
      });

 }

});

router.delete('/:id', authenticate, async(req,res)=>{

 try{

      await prisma.${varName}.delete({

          where:{
              id:req.params.id
          }

      });

      res.json({
          success:true
      });

 }catch(err){

      res.status(500).json({
          error:err.message
      });

 }

});`;

  return `// Auto-generated by AppForge — ${entityName} router
import express from 'express';
import { authenticate, authorize } from '../auth/middleware.js';

const router = express.Router();
${routes}

export default router;
`;
}

// ── Auth Middleware Generator ─────────────────────────────────────────────────

function generateAuthMiddleware(appSpec) {
  const strategy = appSpec.auth?.strategy || 'jwt';
  const jwtExpiry = appSpec.auth?.jwtConfig?.expiry || '7d';
  const roles = appSpec.roles.map(r => r.name);

  return `// Auto-generated by AppForge — Auth Middleware
// Strategy: ${strategy}
import jwt from 'jsonwebtoken';

const JWT_SECRET = process.env.JWT_SECRET || 'change_me_in_production';
const VALID_ROLES = ${JSON.stringify(roles)};

export function authenticate(req, res, next) {
  const header = req.headers['authorization'];
  if (!header || !header.startsWith('Bearer ')) {
    return res.status(401).json({ error: 'Authentication required' });
  }
  const token = header.slice(7);
  try {
    const payload = jwt.verify(token, JWT_SECRET);
    req.user = payload;
    next();
  } catch {
    return res.status(401).json({ error: 'Invalid or expired token' });
  }
}

export function authorize(...allowedRoles) {
  return (req, res, next) => {
    if (!req.user) return res.status(401).json({ error: 'Not authenticated' });
    if (!allowedRoles.includes(req.user.role)) {
      return res.status(403).json({ error: \`Requires role: \${allowedRoles.join(' or ')}\` });
    }
    next();
  };
}

export function generateToken(user) {
  return jwt.sign(
    { id: user.id, email: user.email, role: user.role },
    JWT_SECRET,
    { expiresIn: '${jwtExpiry}', algorithm: '${appSpec.auth?.jwtConfig?.algorithm || 'HS256'}' }
  );
}
`;
}

// ── Page Component Generator ──────────────────────────────────────────────────

